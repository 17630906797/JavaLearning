package com.heima.literal;

public class literalDemo {
    public static void main(String[] args) {
//1.整数
        System.out.println(520);
//        2.小数
        System.out.println(520.1314);
//        3.字符（只能放一个）
        System.out.println('1');
        System.out.println('中');
//        空格也表示一个字符，是空字符
        System.out.println(' ');
//        4.字符串
        System.out.println("abcdef");
    }
}
